    public static void VerDatos() throws ClassNotFoundException, SQLException {
        int idCajaAct = 0;
        idCajaAct = FORM_PRINCIPAL.idcaja;
        try {
            int nf = tblTransacciones.getRowCount();
            for (int i = 0; i < nf; i++) {
                dtm.removeRow(0);
            }
            for (Object[] o : CRUD_OperacionMoneda.ListadeTransacciones(CRUD_Caja.getFechaApertura(idCajaAct), idCajaAct, Ingreso.idusuario)) {
                dtm.addRow(o);
            }
            tblTransacciones.setRowSelectionInterval(tblTransacciones.getRowCount() - 1, tblTransacciones.getRowCount() - 1);
            jScrollPane2.getVerticalScrollBar().setValue(jScrollPane2.getVerticalScrollBar().getMaximum());
        } catch (Exception e) {
            System.err.println("Erro" + e);
        }
    }

